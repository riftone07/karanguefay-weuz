                <h3>La charte des données personnelles</h3>

                <h4>La charte des données personnelles, « charte data friendly » ou encore « charte Informatique et Liberté » se généralise dans les entreprises.</h4>
                <p>A disposition de chacun, elle pose en toute transparence la politique interne à l’entreprise vis-à-vis des données personnelles transmises par le client ou par les salariés : recueil des données, conditions et autorisations d’utilisation...</p>

                <h4>Le contenu de la charte</h4>
                <p>La charte doit réutiliser les éléments de la loi Informatique et Libertés pour présenter :</p>
                <ul>
                    <li>La transparence sur l’utilisation des données collectées, la finalité de la collecte et du traitement,</li>
                    <li>Les informations sur l’identité du responsable de la collecte,</li>
                    <li>Le contrôle complet sur les données – collecte, utilisation, modifications - par son propriétaire, soit la personne identifiée,</li>
                    <li>Les démarches de modifications de données, d’autorisation ou de refus.</li>
                </ul>
                <p>Dans la mesure où la loi Informatique et libertés est française, il est également important de préciser la politique de protection des données personnelles dans les pays de l’Union européenne ou à l’étranger, s’il y a lieu.</p>

                <h4>Les bonnes raisons pour adopter une charte des données personnelles</h4>
                <h3>La méfiance des utilisateurs</h3>
                <p>Les publicités reçues par sms, les newsletters et messages de promos qui inondent une boite mail, les coups de fil à domicile en soirée, ou, plus grave, piratages de code de carte bancaire ou usurpation d’identité... les consommateurs sont souvent victimes d’une trop grande circulation de données. Petites et grandes déconvenues sont relativement fréquentes et créent un climat de méfiance. En 2018, une étude montre que la protection des données est une préoccupation concernant 40 % de sénégalais.</p>

                <h3>Une charte pour la transparence</h3>
                <p>En exposant de manière explicite les droits du consommateur vis-à-vis de ses données, l’entreprise assure sa politique de transparence. Elle montre sa volonté de rester dans le cadre de la loi, voire de renforcer les mesures légales en montrant d’éventuelles mesures propre à l’entreprise –anonymat sur certains accès, annulation de données de plus de 18 mois...-. C’est un moyen de montrer que l’entreprise a les mêmes préoccupations que celles de son client.</p>

                <h3>Une charte pour rassurer</h3>
                <p>La collecte des données et le fichage des clients est un incontournable de certains domaines, en particulier du commerce en ligne. Il est l’un des rares moyens pour attirer l’œil et fidéliser le client. Recevoir des codes promo ou une newsletter intéresse le client... seulement s’il l’a choisi.</p>

